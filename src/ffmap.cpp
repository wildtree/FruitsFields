// Map コード
// 1 ... 壁
// 2 ... REMくん
// 3 ... Fruit
// 4 ... ←
// 5 ... →
// 6 ... ↑
// 7 ... ↓
FFMap::FFMap()
{
    _field = new uint8_t [16 * 10];
}

FFMap::~FFMap()
{
    delete[] _field;
}

uint8_t
FFMap::load(int scene)
{
    uint8_t *data = _map_data[scene];
    _left = 0;

    for (int i = 0 ; i < 80 ; i++)
    {
        uint8_t h = (data[i] >> 4) & 0xf;
        uint8_t l = (data[i] & 0xf);
        _field[2 * i + 0] = h;
        _field[2 * i + 1] = l;
        if (h == 3) ++_left;
        if (l == 3) ++_left;
        if (h == 2||l == 2) {
            _ry = i / 8;
            _rx = (i % 8) * 2;
            if (l == 2) ++_rx;
        }
    }
    _fruit = scene / 5;
    _dir = Left;
    return _left;
}

bool
FFMap::move(Dir dir)
{
    bool moved = false;
    _dir = dir;
    switch(dir)
    {
    case Left:
        if (_rx > 0)
        {
            uint8_t c = _field[(_rx - 1) + _ry * 16];
            if (c == 0||c == 3)
            {
                _field[_rx + _ry * 16] = 0;
                --_rx;
                _field[_rx + _ry * 16] = 2;
                if (c == 3) --_left;
                moved = true;
            }
        }
        break;
    case Right:
        if (_rx < 16 - 1)
        {
            uint8_t c = _field[(_rx + 1) + _ry * 16];
            if (c == 0||c == 3)
            {
                _field[_rx + _ry * 16] = 0;
                ++_rx;
                _field[_rx + _ry * 16] = 2;
                if (c == 3) --_left;
                moved = true;
            }
        }
        break;
    case Up:
        if (_ry > 0)
        {
            uint8_t c = _field[_rx + (_ry - 1) * 16];
            if (c == 0||c == 3)
            {
                _field[_rx + _ry * 16] = 0;
                --_ry;
                _field[_rx + _ry * 16] = 2;
                if (c == 3) --_left;
                moved = true;
            }
        }
        break;
    case Down:
        if (_ry < 10 - 1)
        {
            uint8_t c = _field[_rx + (_ry + 1) * 16];
            if (c == 0||c == 3)
            {
                _field[_rx + _ry * 16] = 0;
                ++_ry;
                _field[_rx + _ry * 16] = 2;
                if (c == 3) --_left;
                moved = true;
            }
        }
    }
    return moved;
}


bool
FFMap::crash(int x, int y, Dir dir)
{
    uint8_t &b = _field[x + y * 16];
    // Serial.printf("block = %d\r\n", b);
    if (dir == Left && b == 4)
    {
        if (x == 0)
        {
            b = 0;
            return true;
        }
        uint8_t c = _field[x - 1 + y * 16];
        // Serial.printf("  left: %d\r\n", c);
        if (c != 0 && c != 5)
        {
            b = 0;
            return true;
        }
    }
    else if (dir == Right && b == 5)
    {
        if (x == 15)
        {
            b = 0;
            return true;
        }
        uint8_t c = _field[x + 1 + y * 16];
        // Serial.printf("  right: %d\r\n", c);
        if (c != 0 && c != 4)
        {
            b = 0;
            return true;
        }
    }
    else if (dir == Up && b == 6)
    {
        if (y == 0)
        {
            b = 0;
            return true;
        }
        uint8_t c = _field[x + (y - 1) * 16];
        // Serial.printf("  up: %d\r\n", c);
        if (c != 0 && c != 7)
        {
            b = 0;
            return true;
        }
    }
    else if (dir == Down && b == 7)
    {
        if (y == 9)
        {
            b = 0;
            return true;
        }
        uint8_t c = _field[x + (y + 1) * 16];
        // Serial.printf("  down: %d\r\n", c);
        if (c != 0 && c != 6)
        {
            b = 0;
            return true;
        }
    }
    // Serial.println("no crash.");
    return false;
}

bool
FFMap::block_move(int &bx, int &by)
{
    uint8_t b = _field[bx + by * 16];
    // Serial.printf("block move (bx, by, b) = (%d, %d, %d)\r\n", bx, by, b);
    if (b < 4 || b > 7) return false; // not a block
    int dy = 0, dx = 0;
    if (b == 4) dx = -1;
    if (b == 5) dx = 1;
    if (b == 6) dy = -1;
    if (b == 7) dy = 1;
    // Serial.printf("(dx, dy) = (%d, %d)\r\n", dx, dy);
    int tx = bx + dx;
    int ty = by + dy;
    if (tx < 0 || tx > 15) return false;
    if (ty < 0 || ty > 9) return false;
    uint8_t c = _field[tx + ty * 16];
    // Serial.printf("(tx, ty, c) = (%d, %d, %d)\r\n", tx, ty, c);
    if (c != 0) return false;
    _field[bx + by * 16] = 0;
    _field[tx + ty * 16] = b;
    bx = tx;
    by = ty;
    return true;
}